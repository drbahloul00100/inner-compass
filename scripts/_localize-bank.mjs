// ---------------------------------------------------------------------------
// Transform: walk source bank, wrap user-facing strings as { en, ar }.
// All other fields are passed through verbatim.
// ---------------------------------------------------------------------------

function localize(en, ar) {
  if (typeof ar !== "string") {
    throw new Error(`Missing Arabic translation for: ${en?.slice?.(0, 60)}...`);
  }
  return { en, ar };
}

function localizeQuestion(q) {
  const t = T[q.id];
  if (!t) throw new Error(`No translation block for question id ${q.id}`);

  const out = {
    id: q.id,
    user_facing_item: localize(q.user_facing_item, t.item),
    answer_type: q.answer_type,
  };

  switch (q.answer_type) {
    case "likert":
    case "directional_likert": {
      const scaleKey = SCALE_BY_ITEM[q.id];
      if (!scaleKey) throw new Error(`No scale key for likert item ${q.id}`);
      const scale = SCALES[scaleKey];
      out.options = {
        scale_labels: {
          "1": scale["1"],
          "2": scale["2"],
          "3": scale["3"],
          "4": scale["4"],
          "5": scale["5"],
        },
      };
      // Validate the source's scale matches what we expect (sanity check).
      const srcScale = q.options.scale_labels;
      for (const k of ["1", "2", "3", "4", "5"]) {
        if (srcScale[k] !== scale[k].en) {
          throw new Error(
            `Scale mismatch for item ${q.id} key ${k}: source="${srcScale[k]}" expected="${scale[k].en}"`
          );
        }
      }
      break;
    }

    case "multiple_choice": {
      if (!t.opts) throw new Error(`Missing opts for multiple_choice item ${q.id}`);
      out.options = q.options.map((o) => ({
        option_id: o.option_id,
        label: localize(o.label, t.opts[o.option_id]),
      }));
      break;
    }

    case "two_part_multiple_choice": {
      if (!t.subs) throw new Error(`Missing subs for two_part item ${q.id}`);
      out.options = {
        sub_prompts: q.options.sub_prompts.map((sp) => {
          const subT = t.subs[sp.sub_id];
          if (!subT) throw new Error(`Missing sub translation for ${sp.sub_id}`);
          return {
            sub_id: sp.sub_id,
            prompt: localize(sp.prompt, subT.prompt),
            context_domain: sp.context_domain,
            options: sp.options.map((o) => ({
              option_id: o.option_id,
              label: localize(o.label, subT.opts[o.option_id]),
            })),
          };
        }),
      };
      break;
    }

    case "free_text": {
      // Free-text has no options labels to translate; only user_facing_item.
      out.options = q.options;
      break;
    }

    default:
      throw new Error(`Unknown answer_type "${q.answer_type}" on item ${q.id}`);
  }

  // Pass-through scoring/visibility fields untouched.
  out.scoring_metadata = q.scoring_metadata;
  out.score_mapping = q.score_mapping;
  out.internal_notes = q.internal_notes;
  out.visibility = q.visibility;

  return out;
}

